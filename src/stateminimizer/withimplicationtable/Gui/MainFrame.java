private void styleMinimizedTable(List<Set<String>> classes,
                                 List<String> repNames) {

    Color[] palette = {
        new Color(214, 234, 248),  // light blue
        new Color(232, 218, 239),  // light purple
        new Color(213, 245, 227),  // light green
        new Color(253, 235, 208),  // light orange
        new Color(250, 219, 216),  // light red
        new Color(210, 241, 234),  // light teal
        new Color(253, 243, 207),  // light yellow
        new Color(234, 237, 237)   // light grey
    };

    minimizedFsmTable.setRowHeight(32);
    minimizedFsmTable.setFont(new Font("Arial", Font.PLAIN, 13));
    minimizedFsmTable.setShowGrid(true);
    minimizedFsmTable.setGridColor(new Color(180, 180, 180));
    minimizedFsmTable.getTableHeader()
            .setFont(new Font("Arial", Font.BOLD, 12));
    minimizedFsmTable.getTableHeader()
            .setBackground(new Color(44, 62, 80));
    minimizedFsmTable.getTableHeader()
            .setForeground(Color.WHITE);
    minimizedFsmTable.getTableHeader()
            .setPreferredSize(new Dimension(0, 35));

    // Color each row by its equivalence class index
    minimizedFsmTable.setDefaultRenderer(Object.class,
        new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable t,
                    Object val, boolean sel, boolean focus,
                    int row, int col) {
                Component c = super.getTableCellRendererComponent(
                        t, val, sel, focus, row, col);
                if (!sel) {
                    // Row index corresponds to equivalence class index
                    c.setBackground(palette[row % palette.length]);
                    if (col == 0) {
                        ((JLabel)c).setFont(
                                new Font("Arial", Font.BOLD, 13));
                    }
                }
                ((JLabel) c).setHorizontalAlignment(JLabel.CENTER);
                return c;
            }
        });

    // Auto-size columns
    minimizedFsmTable.getColumnModel().getColumn(0)
            .setPreferredWidth(70);
    for (int i = 1; i < minimizedFsmTable.getColumnCount(); i++)
        minimizedFsmTable.getColumnModel().getColumn(i)
                .setPreferredWidth(100);
}
    
