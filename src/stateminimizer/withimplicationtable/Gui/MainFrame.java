    private void minimizeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minimizeButtonActionPerformed
    // 1. Validate and build the state machine from the table
    StateMachine machine = transitionController.buildAndValidate();
    if (machine == null) return;  // validation failed

    // 2. Run the implication table algorithm
    ImplicationTable impTable = new ImplicationTable(machine);
    impTable.minimize();

    // 3. Send data to the custom drawing panel (top panel)
    drawPanel.loadData(impTable, machine.getStates());

    // 4. Fill the step-by-step log text area
    stepLogArea.setText("");
    for (String line : impTable.getStepLog()) {
        stepLogArea.append(line + "\n");
    }
    stepLogArea.setCaretPosition(0); // scroll to top

    // 5. Get equivalence classes from the algorithm
    List<Set<String>> classes = impTable.getEquivalenceClasses();

    // 6. Show colored badges in classesPanel
    showEquivalenceClasses(classes);

    // 7. Build the minimized FSM table in the Results tab
    buildMinimizedFsmTable(machine, classes,
            transitionController.getInputSymbols());

    // 8. Switch to the Results tab so user sees output
    resultsTabbedPane.setSelectedIndex(1); // 0=Log, 1=Results

    statusLabel.setText("  ✅ Done! Original: "
            + machine.getNumStates() + " states → Minimized: "
            + classes.size() + " states.");


    }//GEN-LAST:event_minimizeButtonActionPerformed

