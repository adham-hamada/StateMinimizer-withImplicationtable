    private void showEquivalenceClasses(List<Set<String>> classes) {
    classesPanel.removeAll();

    Color[] palette = {
        new Color(52,152,219), new Color(155,89,182),
        new Color(26,188,156), new Color(230,126,34),
        new Color(231,76,60),  new Color(149,165,166)
    };

    int colorIdx = 0;
    for (Set<String> group : classes) {
        // Build label text like "{ S0, S2 }"
        String text = "{ " + String.join(", ", group) + " }";
        JLabel badge = new JLabel(text);
        badge.setOpaque(true);
        badge.setBackground(palette[colorIdx % palette.length]);
        badge.setForeground(Color.WHITE);
        badge.setFont(new Font("Arial", Font.BOLD, 13));
        badge.setBorder(BorderFactory.createEmptyBorder(6, 12, 6, 12));
        classesPanel.add(badge);
        classesPanel.add(Box.createHorizontalStrut(8)); // spacing
        colorIdx++;
    }

    classesPanel.revalidate();
    classesPanel.repaint();
    }
    
    private void buildMinimizedFsmTable(StateMachine originalMachine,
                                    List<Set<String>> classes,
                                    String[] inputSymbols) {

    int numInputs = originalMachine.getNumInputs();

    // ── Step A: Build a map from original state name → representative name
    // The representative is the first state alphabetically in each group
    Map<String, String> representativeOf = new HashMap<>();
    List<String> representativeNames = new ArrayList<>();

    for (Set<String> group : classes) {
        // Use the first element as the representative
        // (LinkedHashSet preserves insertion order from the algorithm)
        String rep = group.iterator().next();
        representativeNames.add(rep);
        for (String member : group) {
            representativeOf.put(member, rep);
        }
    }

    // ── Step B: Build column headers
    // Layout: | State | Next(x=0) | Out(x=0) | Next(x=1) | Out(x=1) | ...
    String[] cols = new String[1 + numInputs * 2];
    cols[0] = "State";
    for (int i = 0; i < numInputs; i++) {
        cols[1 + i * 2]     = "Next (" + inputSymbols[i] + ")";
        cols[1 + i * 2 + 1] = "Out  (" + inputSymbols[i] + ")";
    }

    // ── Step C: Build one row per equivalence class
    Object[][] data = new Object[classes.size()][cols.length];

    for (int r = 0; r < representativeNames.size(); r++) {
        String repName = representativeNames.get(r);
        data[r][0] = repName;   // State column

        // Find this representative in the original machine
        State originalState = originalMachine.getState(repName);

        // If for some reason it's not found, try any member of the group
        if (originalState == null) {
            Set<String> group = classes.get(r);
            for (String member : group) {
                originalState = originalMachine.getState(member);
                if (originalState != null) break;
            }
        }

        if (originalState == null) continue; // safety check

        for (int inp = 0; inp < numInputs; inp++) {
            // Get original next state
            String originalNext = originalState.getNextStates()[inp];
            // Map it to its representative in the minimized machine
            String minimizedNext = representativeOf.getOrDefault(
                    originalNext, originalNext);

            String output = originalState.getOutputs()[inp];

            data[r][1 + inp * 2]     = minimizedNext;
            data[r][1 + inp * 2 + 1] = output;
        }
    }

    // ── Step D: Create a non-editable table model
    DefaultTableModel model = new DefaultTableModel(data, cols) {
        @Override
        public boolean isCellEditable(int row, int col) {
            return false;  // results table is read-only
        }
    };

    minimizedFsmTable.setModel(model);

    // ── Step E: Style the minimized FSM table
    styleMinimizedTable(classes, representativeNames);
}
    
